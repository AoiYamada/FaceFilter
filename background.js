// https://github.com/WebDevSimplified/Face-Recognition-JavaScript/blob/master/script.js
function loadLabeledImages() {
  return Promise.all(
    labels.map(async label => {
      const descriptions = [];
      for (let i = 0; i < 36; i++) {
        const img = await faceapi.fetchImage(`${assetURL}/${label}/${i}.jpg`);
        const detections = await faceapi
          .detectSingleFace(img)
          .withFaceLandmarks()
          .withFaceDescriptor();

        if (detections) {
          descriptions.push(Array.from(detections.descriptor));
        }
      }
      return { label, descriptions };
    })
  );
}
